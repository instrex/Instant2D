namespace Instant2D.Audio {
    public enum PlaybackState {
        /// <summary>
        /// The sound is currently playing.
        /// </summary>
        Playing,

        /// <summary>
        /// The sound was paused.
        /// </summary>
        Paused,

        /// <summary>
        /// The sound is not playing.
        /// </summary>
        Stopped,
    }
}
